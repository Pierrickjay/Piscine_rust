fn fizz_buzz()
{
	for n in 1..100
	{
		match (n % 3, n % 5, (n - 3) % 11, (n - 5) % 11)
		{
			(0, 0, _, _) => println!("fizzbuzz"),
			(0, _, _, _) => println!("fizz"),
			(_, 0, _, _) => println!("buzz"),
			(_, _, 0, _) => println!("FIZZ"),
			(_, _, _, 0) => println!("BUZZ"),
			(_, _, _, _) => println!("{n}"),
		}
	}
}

fn main()
{
	fizz_buzz()
}
