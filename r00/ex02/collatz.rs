fn collatz(start: u32)
{
	let mut test = start;
	while test != 1
	{
		println!("{}", test);
		if test % 2 == 0 {test = test / 2}
		else { test = test * 3 + 1}
	}
	println!("{}", test);
}

fn main()
{
	collatz(5000)
}
