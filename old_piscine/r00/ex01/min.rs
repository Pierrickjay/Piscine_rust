fn min(a: i32, b: i32) -> i32
{
	println!("the value of x = {a} y = {b}");
	if a > b
	{
		b
	}
	else
	{
		a
	}

}

fn main()
{
	let x = 32;
	let y = 42;

	println!("{}", min(x, y));
	println!("{}", min(60, 30));
	println!("{}", min(20, 20));
	println!("{}", min(-8, 9));
	println!("{}", min(10000, 15));



}
